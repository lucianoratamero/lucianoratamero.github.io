<h3>pra quem não quer dor de cabeça e tá vindo do windows:</h3>
<h4>Linux Mint</h4>
<figure><a href={mint}><img src={mint} alt="Linux Mint" /></a></figure>
<p>
  <strong>Linux Mint</strong>
  é a distro desktop mais popular do
  <a href="http://distrowatch.com/">distrowatch</a>, e por um bom motivo: ela é
  uma distro
  <strong>muito</strong>
  familiar pra quem quer sair do windows. ela usa o
  <em>desktop environment (ou seja, interface)</em>
  Cinnamon, desenvolvido pelo próprio pessoal do Linux Mint, com o propósito de
  ser simples, leve e oferecer uma experiência similar à do windows, então é
  díficil pra qualquer um ficar perdido ao usar o Mint. além disso tudo, o Mint
  é feito sobre o Debian e o Ubuntu, o que traz pro Mint uma gigantesca
  comunidade e programas usados em tudo que é lugar.
</p>
<p>site: <a href="http://linuxmint.com/">http://linuxmint.com/</a></p>
<p>
  download:
  <a
    href="http://www.linuxmint.com/download.php">http://www.linuxmint.com/download.php</a>
</p>
<h3>pra quem não quer dor de cabeça, mas não liga pra interface</h3>
<h4>Ubuntu</h4>
<figure><a href={ubuntu}><img src={ubuntu} alt="Ubuntu" /></a></figure>
<p>
  o
  <strong>Ubuntu</strong>
  é praticamente onipresente no vocabulário dos linuxeiros. conhecido por sua
  interface personalizada, por decisões duvidosas e por ter versões populares
  para uso em servidores, o Ubuntu é uma ótima pedida pra quem tá começando com
  linux e quer algo diferente. a comunidade é absurdamente grande e as novas
  versões têm longos anos de garantia de suporte. vale a pena dar uma olhada ;D
</p>
<p>site: <a href="http://www.ubuntu.com/">http://www.ubuntu.com/</a></p>
<p>
  download:
  <a
    href="http://www.ubuntu.com/download/desktop">http://www.ubuntu.com/download/desktop</a>
</p>
<h4>Fedora</h4>
<figure><a href={fedora}><img src={fedora} alt="Fedora" /></a></figure>
<p>
  quando se fala de linux, é impossível fugir do
  <strong>Fedora</strong>. o Fedora é uma distro sólida desenvolvida pela
  <a href="http://www.redhat.com/">Red Hat</a>, uma das maiores empresas de
  código aberto do mundo. geralmente, o Fedora oferece uma experiência mais
  clássica, no sentido de que não há praticamente nada que seja especificamente
  criado pra distribuição, o que faz com que você tenha nas mãos as melhores
  ferramentas criadas pela comunidade. normalmente traz programas mais recentes
  que os oferecidos pelas LTSs do Ubuntu.
</p>
<p>site: <a href="https://getfedora.org/">https://getfedora.org/</a></p>
<p>
  download:
  <a
    href="https://getfedora.org/pt_BR/workstation/download/">https://getfedora.org/pt_BR/workstation/download/</a>
</p>
<h3>pra quem não quer dor de cabeça, mas algo lindo <span>*-*</span></h3>
<h4>elementaryOS</h4>
<figure>
  <a href={elementary}><img src={elementary} alt="elementaryOS" /></a>
</figure>
<p>
  em matéria de beleza e funcionalidade básica, ninguém,
  <strong>ninguém</strong>
  ganha do elementaryOS. criado com o design e a usabilidade como prioridades, o
  elementary deixa qualquer usuário de Mac OSX babando; tudo isso com software
  100% aberto. ele é baseado no Ubuntu, então traz consigo toda uma comunidade
  pronta pra dar suporte. já falei que ele é
  <strong>lindo</strong>?
</p>
<p>site: <a href="https://elementary.io">https://elementary.io</a></p>
<p>
  download:
  <a href="https://elementary.io">https://elementary.io</a>
  (você pode optar por pagar R$ 0, ele deixa!)
</p>
<h3>pra quem já é usuário de linux e quer algo novo</h3>
<h4>Manjaro</h4>
<figure><a href={manjaro}><img src={manjaro} alt="Manjaro" /></a></figure>
<p>
  <strong>Manjaro</strong>
  é uma distro relativamente nova, baseada em Arch Linux, que vem ganhando
  bastante força. ela é fácil de instalar e vem em milhões de diferentes
  <em>flavours</em>: xfce, kde, cinnamon, gnome, fluxbox, bspwm, openbox, mate,
  deepin...
  <strong>vários</strong>
  <em>flavours</em>. é a distro que eu uso no dia-a-dia, por oferecer as
  ferramentas do Arch (como o AUR) com uma maior estabilidade e uma grande
  facilidade de instalação e configuração. pra quem entende de linux, vale
  <strong>muito</strong>
  dar uma olhada.
</p>
<p>site: <a href="http://manjaro.github.io/">http://manjaro.github.io/</a></p>
<p>
  download:
  <a
    href="http://manjaro.github.io/download/">http://manjaro.github.io/download/</a>
</p>
<h4>Apricity OS</h4>
<figure><a href={apricity}><img src={apricity} alt="Apricity" /></a></figure>
<p>
  o
  <strong>Apricity</strong>
  é um dos desconhecidos. ele oferece também vários
  <em>flavours</em>, também é baseado em Arch e
  <strong>também</strong>
  é lindo, oferecendo interfaces bem diferenciadas. ele vem com umas
  sacanageiras, como integração com o PushBullet e com o Evernote, o que deixa
  ele bem fora da mente de quem quer só software livre rodando. o único grande
  problema dele é que ele, bem, é desconhecido, então não dá pra ter
  previsibilidade dele ser mantido a longo prazo. ele tem o diferencial de só
  ter um live USB pra todas as diferentes interfaces, mas requer internet para
  poder baixar os programas corretos na hora da instalação. enfim, algo novo,
  vale estar no radar.
</p>
<p>site: <a href="https://apricityos.com/">https://apricityos.com/</a></p>
<p>download: <a href="https://apricityos.com/">https://apricityos.com/</a></p>
<h3>pra quem quer algo hardcore</h3>
<h4>manjaro - versão i3</h4>
<figure>
  <a href={manjaroi3}><img src={manjaroi3} alt="Manjaro i3" /></a>
</figure>
<p>
  essa é pra quem quer um desafio. o manjaro em si já é uma distro que precisa
  de alguém minimamente experiente pra saber usar seu potencial; quando falamos
  de uma versão preconfigurada com o i3 então, chega a outro nível. o
  <a href="https://i3wm.org/">i3wm</a>
  é um
  <em>tiling window manager</em>
  (se você não sabe o que isso é, sério, pula essa) desenvolvido por uns malucos
  da Google. se você quer algo leve (o desktop environment inteiro pesa menos de
  10Mb), rápido (respostas em menos de 100ms!), baseado em teclado e não tem
  problema em perder horas e horas configurando, essa é uma ótima pedida. uso no
  meu celeron de 7 anos atrás, funciona lindamente =)
</p>
<p>site: <a href="http://manjaro.github.io/">http://manjaro.github.io/</a></p>
<p>
  download:
  <a
    href="https://sourceforge.net/projects/manjarolinux/files/community/i3/">https://sourceforge.net/projects/manjarolinux/files/community/i3/</a>
</p>
