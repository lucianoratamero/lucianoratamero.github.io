<p>
  hoje eu tava conversando com uns amigos sobre git e github e percebemos um
  problema: não temos atualmente um lugar com conteúdo sobre os dois em
  português, principalmente para leigos ou iniciantes. depois de pegar com o
  pessoal uns pontos importantes, decidi fazer alguns posts sobre o assunto,
  para servir de referência. aí vamos nós o/
</p>
<hr />

<h3>o que é o git?</h3>
<figure>
  <img
    src="https://lucianoratamero.github.io/img/git_logo.png"
    alt="Logo do Git" />
</figure>
<p>
  <a href="https://git-scm.com/">git</a>
  é uma
  <strong>ferramenta para versionamento de arquivos</strong>.
</p>
<p>
  um exemplo: digamos que você queira escrever um poema pro seu amorzinho e
  tenha várias ideias. você escreve um verso, escreve outro e pronto, taí seu
  poema, o
  <code>amorzinho.txt</code>. você manda o arquivo pro seu amorzinho, que fica
  apaixonado. depois de alguns dias, você tem uma ideia nova, volta no texto,
  reescreve um verso e manda pro seu amorzinho de novo, esperando coraçõezinhos
  de carinho. mas seu amorzinho diz "a versão antiga era tão mais bonita... tem
  como me mandar a antiga?". mas, oh!, você esqueceu como o texto era e não tem
  mais salvo em lugar nenhum! aí que entra o git.
</p>
<p>
  o git serve para que você possa criar uma linha do tempo com diferentes pontos
  das alterações feitas. assim, você consegue manter o histórico das alterações
  de um mesmo arquivo ou arquivos ao longo do tempo.
</p>
<p>
  nem preciso dizer o quanto isso é
  <strong>crucial</strong>
  para projetos de software, já que esses projetos exigem que você e sua equipe
  criem novas funcionalidades nos mesmos arquivos, com uma versão antiga salva
  pro caso da nova dar problema. vou explicar melhor
  <strong>como</strong>
  ele funciona daqui a pouco, mas, antes, tenho que explicar o que é o github.
</p>
<h3>o que é o github?</h3>
<figure>
  <img
    src="https://lucianoratamero.github.io/img/github_logo.png"
    alt="Logo do Github" />
</figure>
<p>
  <a href="https://github.com/">github</a>
  é só um
  <strong>site</strong>
  no qual você pode fazer
  <strong>upload de projetos git</strong>.
</p>
<p>
  o github (palavra que seria traduzida como "agregador de gits") é o site mais
  conhecido de hospedagem de projetos git. ele serve para que você e sua equipe
  possam manter as versões do projeto em que trabalham em um lugar centralizado,
  na internet, acessível a quem pertence ao projeto.
</p>
<p>
  além de hospedar projetos git, o github dá visibilidade da linha do tempo de
  diferentes versões do projeto, provê ferramentas de métricas (como quais
  usuários alteraram mais o projeto, quando alteraram, o que alteraram, etc),
  gerência de problemas relatados (<em>tickets</em>
  ou
  <em>issues</em>), dentre outras. vou detalhar um pouco melhor daqui a pouco.
</p>
<div class="callout text-center">
  <p><strong>lembrem-se:</strong></p>
  <p>
    a diferença entre git e github é que
    <strong>o git é só uma ferramenta para versionar projetos</strong>, enquanto
    <strong>o github é o site no qual você colocará esses projetos versionados</strong>.
    uma analogia válida seria que o git é seu pincel e tintas enquanto o github
    é um museu.
  </p>
</div>
<h3>beleza, mas como o git funciona?</h3>
<p>
  vou tentar explicar conceitos básicos, mas não vou fazer um tutorial de como
  usar a ferramenta, porque
  <a href="https://www.atlassian.com/git/tutorials/">tem</a>
  <a href="https://try.github.io/levels/1/challenges/1">vários</a>
  <a href="https://rogerdudler.github.io/git-guide/">por</a>
  <a
    href="https://git-scm.com/book/pt-br/v1/Primeiros-passos-No%C3%A7%C3%B5es-B%C3%A1sicas-de-Git">aí</a>.
</p>
<p>
  bem, digamos que você tenha uma pasta com seus textos, ok? seria muito legal
  saber, ao longo do tempo, como foi sua evolução como escritor, ou seja, quais
  textos foram adicionados, removidos ou alterados. você decide começar a usar o
  git na pasta de textos e quer salvar o estado atual da pasta.
</p>
<p>pra isso, você cria um <strong>commit</strong> inicial.</p>
<p>
  um
  <strong>commit</strong>
  é um documento que descreve uma alteração, um ponto na linha do tempo do
  projeto. ele é como um registro de uma alteração no projeto. essa alteração é
  salva com seu nome, seu email, data da alteração, suas alterações na pasta e
  uma descrição da alteração, que você mesmo escreve.
</p>
<figure>
  <img
    src="https://lucianoratamero.github.io/img/commit.jpg"
    alt="Ilustração de um registro de commit" />
</figure>
<p>
  então, na hora que você coloca os textos na pasta pela primeira vez, você cria
  um commit com a descrição "adiciona os textos que tenho agora", seleciona
  todos os arquivos e pronto, você tem um ponto na linha do tempo!
</p>
<h3>
  ok, mas agora quero usar o github pra salvar meus textos na nuvem? #comofas
</h3>
<p>
  pra isso, você cria uma conta no github e cria o que chamamos de um
  <strong>repositório</strong>.
</p>
<p>
  na linguagem do povo de computação,
  <strong>repositórios</strong>
  são
  <strong>locais nos quais colocamos arquivos na internet</strong>. eles servem
  para que qualquer um consiga
  <strong>repor</strong>
  o código de um projeto no seu computador, caso você não tenha ele salvo. isso
  significa que, quando você for colocar seu projeto no github, você primeiro
  precisa criar um repositório lá.
</p>
<p>
  com o repositório criado no github, você só precisa configurar
  <strong>onde</strong>
  está seu repositório e
  <strong>empurrar</strong>
  as
  <strong>alterações</strong>
  pra lá. ou seja, você define a
  <strong>origem</strong>
  do repositório e faz um
  <strong>push</strong>
  dos
  <strong>commits</strong>
  pra lá.
</p>
<figure>
  <img
    src="https://lucianoratamero.github.io/img/repositorio.jpg"
    alt="Ilustração de um repositório, como se fosse nuvem" />
</figure>
<h3>
  legal, agora tenho os meus textos salvos no github. que que eu faço com isso?
</h3>
<p>
  agora, com os textos lá, você pode fazer várias coisas. vou enumerar algumas,
  as mais básicas e úteis. :)
</p>
<h4>clonagem de repositórios</h4>
<p>
  digamos que você foi passar o fim de ano na casa dos seus pais e você quer
  alterar algum texto. você sabe que tem todos eles no seu computador, mas você
  (sua anta) esqueceu de trazê-lo. você até pode copiar os textos direto do site
  do github pro computador da sua mãe, mas isso significa que, se você quiser
  salvar a nova versão lá, você vai ter que criar
  <strong>outro</strong>
  repositório. relaxa, pra isso serve a clonagem de repositórios.
</p>
<figure>
  <img
    src="https://lucianoratamero.github.io/img/clone.jpg"
    alt="Ilustração de um clone de um repo, uma cópia" />
</figure>
<p>
  <strong>clonar</strong>
  um repositório significa
  <strong>baixar o projeto git inteiro, direto da origem, pro seu computador</strong>.
  isso quer dizer que
  <strong>todas as alterações</strong>, de
  <strong>toda a linha do tempo</strong>, estão agora no seu computador. assim,
  ao você clonar um repositório, você pode simplesmente
  <strong>criar novos commits</strong>
  e
  <strong>dar push</strong>
  das novas alterações.
</p>
<p>
  isso permite que uma equipe possa fazer alterações no projeto, cada um em seu
  computador, e possa sincronizar essas alterações através de
  <strong>pushs (empurrões)</strong>
  e
  <strong>pulls (puxadas)</strong>
  de
  <strong>commits (alterações)</strong>
  de um mesmo
  <strong>repositório (projeto)</strong>. é assim que uma equipe de software
  trabalha hoje em dia. :)
</p>
<h4>forks de repositórios</h4>
<p>
  ok, digamos que seu amigo tem um amorzinho também e quer escrever aquele poema
  lindo, mas não tem muita criatividade. você diz pra ele "cara, pode usar o
  amorzinho.txt, mas tem que dar uma alterada, senão vai pegar mal". você não
  quer, no entanto, que seu amiguinho use seu repositório, porque, né, é seu,
  com seus textos. o que seu amigo pode fazer pra ter a versão dele dos seus
  textos é criar o que chamamos de
  <strong>fork</strong>.
</p>
<figure>
  <img
    src="https://lucianoratamero.github.io/img/fork.jpg"
    alt="Ilustração de um fork" />
</figure>
<p>
  um
  <strong>fork</strong>
  é um repositório criado a partir de outro. tudo nele é literalmente igual,
  desde o conteúdo dos arquivos até os commits salvos; a única diferença é que
  <strong>pertence a um usuário diferente</strong>.
</p>
<p>
  no entanto, a utilidade de um fork só se revela totalmente quando começamos a
  falar sobre
  <strong>issues e pull requests</strong>.
</p>
<h4>issues e pull requests</h4>
<p>
  seu amigo, ao ler o seu poema
  <code>amorzinho.txt</code>, percebe que você escreveu errado o nome do seu
  amorzinho. ele quer te avisar que isso precisa ser corrigido, porque isso é um
  <strong>problema</strong>, uma
  <strong>issue</strong>.
</p>
<p>
  uma
  <strong>issue</strong>
  é um relatório de um problema que precisa ser corrigido. imaginemos que seu
  site, por exemplo, tem um link quebrado. qualquer um pode
  <strong>abrir uma issue</strong>
  no seu
  <strong>repositório no github</strong>
  dizendo que o link da página tal está quebrado.
</p>
<p>
  o problema é que você, mantenedor do projeto, tá extremamente ocupado dando
  aulas pro seu amigo de como escrever um poema romântico. você vai lá e
  responde pro cara, na própria issue, que você tá enrolado demais, mas que
  ficaria muito agradecido caso o cara pudesse corrigir esse problema pra você.
</p>
<p>
  o cara é esperto: ele cria um
  <strong>fork</strong>
  do repositório, localiza o arquivo problemático, corrige o link, salva,
  <strong>cria um commit</strong>,
  <strong>faz um push</strong>
  das alterações
  <strong>pro fork dele</strong>, tudo certo. só um detalhe: a correção
  <strong>precisa ser misturada com o seu</strong>
  repositório<strong>. pra isso, ele cria um </strong>pull request<strong>, ou
    seja, um
  </strong>pedido de empurrão de alterações**.
</p>
<p>
  nesse pull request, constam os
  <strong>commits</strong>
  feitos, os
  <strong>arquivos alterados</strong>
  e uma descrição da correção. mas digamos que o cara esqueceu de alterar em
  outro lugar; você comenta no pull request dizendo que precisa alterar no outro
  lugar.
</p>
<figure>
  <img
    src="https://lucianoratamero.github.io/img/pull_request.jpg"
    alt="Ilustração de um pull request" />
</figure>
<p>
  esse detalhe deixa claro que
  <strong>pull requests também são issues</strong>. ambos são
  <strong>discussões sobre um problema</strong>. mas, claro,
  <strong>issues não são necessariamente pull requests</strong>, porque
  <strong>pull requests têm como objetivo a correção de uma ou mais issues</strong>.
</p>
<h3>ufa. tá tenso. mas ok, vamo lá, mais alguma coisa?</h3>
<p>
  só mais uma, juro, tá acabando! digamos que você e seu amigo querem fazer
  diferentes versões de um mesmo poema num mesmo repositório. se você alterar e
  fizer um commit, seu amigo vai acabar fazendo outro commit por cima do seu, e
  isso não é legal. o que você pode fazer é
  <strong>criar uma bifurcação no repositório</strong>
  e dizer pro seu amigo que o seu branch é o seu e o dele é o dele. assim,
  enquanto você faz commits em uma bifurcação, seu amigo faz em outra, sem que
  uma interfira na outra. isso significa que, agora, seu projeto tem
  <strong>dois branchs</strong>
  (tecnicamente, três, porque tem o branch base, né).
</p>
<figure>
  <img
    src="https://lucianoratamero.github.io/img/branch.jpg"
    alt="Ilustração de uma branch, um ramo/galho/bifurcação" />
</figure>
<p>
  <strong>branchs</strong>
  são
  <strong>bifurcações ou ramificações</strong>
  dentro de um projeto git. eles servem para que tenhamos diferentes versões
  baseadas em um mesmo ponto do tempo. são usados para que a equipe se organize,
  para que um desenvolvedor faça a correção do link em um branch, enquanto outro
  desenvolvedor continua commitando em outro branch, sem interferirem um no
  processo do outro.
</p>
<h3>tl;dr (resumo)</h3>
<p>
  bem, falei até demais, mas acho que valeu a pena. vamos resumir alguns termos
  então.
</p>
<h4>git</h4>
<p>
  <strong>git</strong>
  é um
  <strong>programa</strong>, uma
  <strong>ferramenta</strong>
  para versionamento de arquivos. serve para que você tenha uma linha do tempo
  de alterações de uma pasta, de um projeto.
</p>
<h4>commit</h4>
<p>
  um
  <strong>commit</strong>
  é um ponto da linha do tempo de um projeto git. ele descreve as alterações
  feitas num ponto do tempo, criando um
  <strong>snapshot</strong>
  do projeto.
</p>
<h4>repositório</h4>
<p>
  um
  <strong>repositório</strong>
  é um
  <strong>local na internet</strong>
  onde podemos colocar nosso projeto git.
</p>
<h4>github</h4>
<p>
  o
  <strong>github</strong>
  é o site mais famoso de hospedagem de repositórios.
</p>
<h4>clonagem</h4>
<p>
  a
  <strong>clonagem</strong>
  de repositórios é a ação que cria no seu computador local uma cópia de um
  projeto git ligada diretamente a um repositório. permite que você possa
  rapidamente criar um novo commit e empurrar as alterações diretamente para seu
  repositório.
</p>
<h4>push e pull</h4>
<p>
  <strong>push</strong>
  e
  <strong>pull</strong>
  são as ações mais feitas para se sincronizar o estado de um projeto git. com o
  <strong>push</strong>
  você
  <strong>empurra</strong>
  os commits feitos localmente e com o
  <strong>pull</strong>
  você pega as alterações que estão no repositório.
</p>
<h4>forks</h4>
<p>
  <strong>forks</strong>
  são repositórios criados a partir de outro. servem para que um usuário possa
  ter sua própria cópia de um repositório, para que faça suas próprias
  alterações.
</p>
<h4>issues</h4>
<p>
  <strong>issues</strong>
  são tickets, relatos de problemas de um projeto. permitem discutir um bug,
  propor uma nova funcionalidade, etc.
</p>
<h4>pull requests</h4>
<p>
  <strong>pull requests</strong>
  são
  <strong>issues* que contém </strong>commits<strong>
    que solucionam
  </strong>issues<strong> ou </strong>adicionam funcionalidades**. também servem
  para discutir problemas, soluções e também para revisar alterações feitas.
</p>
<h4>branchs</h4>
<p>
  <strong>branchs</strong>
  são
  <strong>bifurcações</strong>
  em um projeto git. servem para que pessoas possam trabalhar em paralelo em um
  mesmo projeto sem medo de sobrescrever ou alterar algo que outra pessoa está
  fazendo.
</p>
