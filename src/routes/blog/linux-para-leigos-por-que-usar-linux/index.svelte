<h3>o que é o linux?</h3>
<p>
  quando falamos de
  <strong>linux</strong>, estamos falando de
  <strong>sistemas operacionais baseados em linux</strong>.
</p>
<h4>
  pera, já começou complicado, o que raios é um sistema operacional? e como
  assim baseado em linux?
</h4>
<p>
  caaaalma, caaaalma, vamos por partes. um
  <strong>sistema operacional</strong>
  não é nada mais que um conjunto de
  <strong>programas</strong>
  que fazem a
  <strong>ponte entre o que o seu computador entende (zeros e uns) e o que os
    seres humanos entendem (textos, sons, imagens, vídeos)</strong>.
</p>
<p>
  eles fazem com que, quando você movimente o mouse, a imagem do cursor mude de
  lugar na tela, por exemplo. lidam com todas as camadas: a sensibilidade de ver
  que o mouse mexeu, a interpretação disso como uma intenção de mover o cursor,
  a atualização do local onde o cursor está e a exibição dele no novo lugar da
  tela. não parece, mas essa é uma operação mega complexa. enfim, eles fazem
  isso, e também dão recursos para que
  <strong>outros programas possam ser instalados</strong>
  e usados nesse computador.
</p>
<h4>hmm, saquei, então por isso que linux é tipo o windows, né?</h4>
<p>
  isso, ele é igual ao windows no sentido de que você primeiro precisa instalar
  ele, o sistema operacional, antes de instalar todos os outros programas,
  exatamente por ele ser essa ponte. mas, é claro, se o linux não fosse
  diferente o suficiente, eu não estaria explicando, né. então vamos pras
  diferenças reais!
</p>
<h3>instalação de programas</h3>
<p>
  uma das primeiras grandes vantagens do linux é seu modo de instalar programas.
  em todas as grandes
  <strong>distribuições linux</strong>, o sistema já vem com um programa que
  <strong>literalmente</strong>
  te deixa instalar qualquer programa disponível.
  <strong>no mundo. <em>sim. isso mesmo.</em></strong>
  cabou superdownloads, cabou o negócio de você instalar uma toolbar sem querer
  só por causa do winrar, cabou essa caça por lugares confiáveis pra baixar os
  programas. (ah, chamamos de distribuição linux um sistema qualquer que use
  linux. leia-se somente
  <em>linux</em>)
</p>
<a href={instaladordeprogramas}>
  <figure>
    <img
      src={instaladordeprogramas}
      alt="Imagem da interface do instalador de programas" />
  </figure>
</a>
<p>
  basicamente, toda distribuição tem seus
  <strong>repositórios de softwares</strong>. pense nisso como o seguinte: toda
  distribuição tem um
  <strong>gigantesco</strong>
  servidor com
  <strong>todos os programas nele</strong>. daí, quer instalar alguma coisa, só
  abrir o programa de instalar programas, buscar o que quer e pronto, instalado.
  fácil, limpo, sem medo de ser feliz (e sem
  <a
    href="http://olhardigital.uol.com.br/noticia/saiba-como-remover-o-hao123-do-computador/44048">pegadinha
    do Hao123</a>).
</p>
<p>
  o único problema é que programas patenteados
  <em>(estou olhando pra você, Adobe)</em>
  não podem ser distribuídos dessa forma. isso significa
  <strong>nada de photoshop, nada de office</strong>. mas, relaxa,
  <strong>existem programas extremamente competentes</strong>
  em fazer o trabalho que esses aí fazem; só é necessário que você pare pra
  aprender como mexer neles, assim como aprendeu a mexer no office. e, olha,
  geralmente eles são
  <strong>bem parecidos</strong>
  (como o
  <a href="https://www.wps.com/linux">WPS Office</a>, por exemplo) e
  compatíveis. e o melhor,
  <strong>tudo grátis, pra sempre grátis</strong>.
</p>
<h3>velocidade</h3>
<p>
  cara, eu tenho um computador de 7 anos atrás, um
  <strong>celeron</strong>
  bosta, todo ferrado, mais lento que meu smartphone, e ele roda linux
  <strong>como um campeão</strong>. se você não entendeu, te digo então:
  <strong><em>linux é rápido pra cacete</em></strong>. então se você quer usar
  seu laptop por mais, sei lá, quatro anos, mas ele tá lento, coloca linux nele.
  não vai se arrepender.
</p>
<p>
  ah, só pra dizer, seu smartphone também é linux, provavelmente. é android?
  sim, android é linux. a google foi muito esperta mesmo.
</p>
<h3>segurança</h3>
<p>
  como eu disse ali em cima, você nunca mais vai cair nas pegadinhas de
  instaladores do windows. além disso, como toda a instalação está centralizada
  em um programa só, ele sabe perfeitamente quando tem que atualizar os
  programas, caso algum deles deixe alguma brecha de segurança aberta.
</p>
<p>
  mas o que mais explica a segurança dos sistemas linux é o fato de que,
  geralmente, todos os programas são, pelo menos,
  <strong>open source</strong>.
</p>
<h4>
  como assim? ter o código inteiro do programa aberto pra qualquer um ver não
  significa que qualquer um pode ver as falhas dele?
</h4>
<p>
  é, parece um paradoxo, né? engraçado, mas o fato de ter as falhas expostas só
  faz com que os programas sejam
  <strong>ainda mais seguros</strong>. já que as falhas estão expostas, qualquer
  um pode apontar a falha, e isso faz com que as brechas de segurança sejam
  consertadas
  <strong>em questão de minutos</strong>. porque esse é o ponto principal: já
  que é a
  <strong>própria comunidade</strong>
  que vê e corrige os problemas de forma
  <strong>voluntária</strong>, o único motivo que sobra para consertar algo é,
  bem,
  <em>ter algo consertado</em>.
</p>
<p>
  no windows, quando um problema é encontrado, a microsoft tem que
  <strong>pagar alguém</strong>
  pra ir lá e consertar; ou seja, como uma empresa, ela geralmente prefere
  <strong>esconder suas falhas</strong>
  pra não gastar dinheiro do que pagar alguém pra consertar. isso significa que
  uma
  <strong>falha de segurança</strong>
  que está lá, no seu computador,
  <strong>há anos</strong>, pode estar sendo explorada por alguém, pelo simples
  fato de que a microsoft quer economizar grana e não deixa o código do windows
  aberto.
</p>
<p>yep, abrir o código te dá mais segurança. <em>deal with it.</em></p>
<h3>drivers? que drivers?</h3>
<p>
  falando em windows, quem aqui nunca teve problema de drivers levanta a mão!
  sabe quando você compra um computador e ele vem cheio de programa aleatório
  com nome de quinze empresas, e se você quiser tirar, se ferra aí? esses são
  nossos amigos
  <strong>drivers</strong>
  (mentira, são inimigos mesmo).
</p>
<p>
  outro cenário: deu pau no seu HD e você tem que formatar tudo?
  <strong><em>oh, noez,</em></strong>
  agora tá tudo zoado, nem o som funciona direito, a tela tá com tudo grande, a
  wifi não funciona.
  <em>drivers, man, friggin drivers.</em>
</p>
<p>
  cabou. linux tem disso não. instalou o sistema operacional, tá tudo
  funcionando. mesmo. tipo, cabou drivers, não tem mais drivers.
  <a href="https://www.youtube.com/watch?v=z64LKnEAI7k">morreu. feneceu. tá
    enterrado.</a>
  assim,
  <em>até tem</em>, e de vez em quando
  <em>sim, dá problema</em>, mas, sério, faz
  <strong>muito</strong>
  tempo que não vejo gente reclamando de drivers quando tá usando linux (fora o
  pessoal que é chato e fica mexendo onde não deve, né, Rômulo?). isso significa
  que
  <strong>você pode instalar linux num pendrive e usar como computador</strong>.
  sério! eu já fiz isso, foi super de boa :D
</p>
<h3>suporte e comunidade</h3>
<p>
  eu já disse que é tudo grátis, né? então, o suporte também é grátis. na
  verdade,
  <a
    href="http://www.diolinux.com.br/2016/02/russia-vai-migrar-para-linux.html">tem
    <strong>muita</strong>
    repartição pública pelo mundo</a>
  que tá trocando pra linux exatamente porque tudo se atualiza automaticamente,
  e tudo é grátis, sem ligação direta com nenhuma empresa. eu lembro que
  <a href="http://www.ic.uff.br/index.php/pt/laboratorios/graduacao">até a UFF
    já usa linux em seus laboratórios de informática</a>
  pra não precisar pagar os
  <strong>rios de dinheiros</strong>
  em licensas do windows.
</p>
<p>
  bem, de vez em quando você vai ter um problema ou outro, e isso é normal com
  qualquer tipo de computador e sistema. mas aí vem outra parte importante do
  mundo linux:
  <strong>a comunidade</strong>. se você tá tendo um problema, cara,
  <strong>alguém</strong>
  já teve esse problema e provavelmente
  <strong>já resolveu</strong>. uma bela busca no google e pronto, você mesmo
  consertou seu pc. no pior dos piores casos, é só você instalar o linux de
  novo, o que é mais fácil que entender como funciona o jogo do truco (sério,
  não entendo truco até hoje!).
</p>
<h3>personalização</h3>
<p>
